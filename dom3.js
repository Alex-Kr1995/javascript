// Переходим к CSS: 
const a = document.querySelector('.testLink1');
const a1 = document.querySelector('.testLink2');
a.style.color = 'green'; // Вот так для свойств из одного слова
a.style.margin = '10px'; // Вот так для свойств из одного слова
a.style.backgroundColor = 'lightgrey'; // Вот так для свойств из двух слов
a.style['border-radius'] = '20%'; // Или вот так для свойств из двух слов, но сложнее чем предыдущий
a.style.border = '1px solid black'; //Вот так для свойств из одного слова
// Если же мы сразу знаем что нам нужно много разных стилей накинуть, то есть свойство cssText:
a1.style.cssText = ` color: white; 
padding: 5px 50px 5px 50px;
margin: 10px;
background-color: black;
text-decoration: none;
`
// Тут уже нам без разницы из одного или же нескольких слов задается свойство, можем просто писать обычный css
// ========================================================================================================================
// Так-же мы можем, и на самом деле в практике это очень часто делают, добавлять или удалять классы
//  className - позвляет назначить класс, зачастую юзаем для новосозданных html элементов
// Для добавления или удаления нового класса лучше использовать classList (дает доступ к массиву классов)
console.log(`classList: ${a1.classList}`)
//------------------------------------------
// classList (add, remove, toggle, contains)
a1.classList.add('someClass') // Сделает текст большим  (мы это указали в dom3.css)
a1.classList.remove('testLink2') // Удаляем класс по которому нашли элемент, LOL XD
console.log(`Before toggle ${a1.classList.contains('testLink2')}`) // Проверяем наличие класса (тоже часто нужно): false (мы его удалили строчкой выше)
a1.classList.toggle('testLink2') // Если класс есть - удаляет его, если нет - добавляет (по-сути как переключатель)
console.log(`After toggle ${a1.classList.contains('testLink2')}`) // Проверяем наличие класса снова: true
//-----------------------------------------
// Ну и естественно, если сильно надо - мы можем менять классы через setAttribute, т.к. класс это такой же аттрибут