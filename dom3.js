const checkbox = document.querySelector('[name="my-checkbox"]') //Вот так ищем по аттрибуту name наш чекбокс
// ========================================================================================================================
// В первую очередь поработаем с аттрибутами. Для этого у нас есть
// setAttribute - установить аттрибут
// hasAttribute - проверить наличие аттрибута
// getAttribute - получить аттрибут
// removeAttribute - удалить аттрибут
// ========================================================================================================================
console.log(checkbox.getAttribute('type')) // checkbox
console.log(checkbox.getAttribute('name')) // my-checkbox
console.log(checkbox.getAttribute('checked')) // выводит пустую строку, для наглядности лучше проверять наличие: 
console.log(checkbox.hasAttribute('checked')) // Тогда в консоли явно будет true


// Хоть мы и рассматриваем на чекбоксе, на самом деле мы можем получить значение любого аттрибута у любого элемента: 
const myLink = document.querySelector('.testLink');
console.log(myLink.getAttribute('href')) //  в консоли будет - https://google.com
// ========================================================================================================================
// Раз мы можем получить аттрибут, значит и установить его новое значение тоже, поэтому пусть наша ссылка на гугл теперь ведет
// в другое место, например на википедию:
myLink.setAttribute('target', '_blank') ; // чтобы открывалось в новом окне

const linkChanger = () => {
    myLink.setAttribute('href', 'https://ru.wikipedia.org/');
    myLink.textContent = 'Вики';
}
setTimeout(linkChanger, 3000);
// ========================================================================================================================
// Проверка аттрибута. Иногда нам в зависимости от того, есть ли аттрибут нужнно что-то сделать с элементом
// hasAttribute - возвращает true/false

myLink.hasAttribute('href') ? console.log('Ссылка есть') : console.log('Ссылка отсутствует'); // Например вот так
// Ну или если аттрибута нет - мы его можем назначить, например. 
// ========================================================================================================================
// Ну и удалить аттрибут мы конечно же можем:
const remHandler = () => myLink.hasAttribute('href') ? myLink.removeAttribute('href') : console.log('Нечего удалять'); // 
setTimeout(remHandler, 10000) // через 10 секунд если не успел нажать на ссылку гугла или вики - останешься с носом)) 

// ========================================================================================================================
// Кроме установки стандартных аттрибутов через js мы можем установить свои собственные, т.е. выдумать аттрибут и придумать 
// его поведение, например которое будет на чекбокс устанавливать время нажатия на него или еще чего. Зависит от того что надо
// Но для этого есть правило - кастомные аттрибуты должны начинаться с data-my_custom_attribute. Т.е. "data-" указывает что
// это наш собственный кастомный аттрибут для наших потребностей. Так-же мы можем получить все кастомные аттрибуты у элемента:
myLink.setAttribute('data-test_attr', 'test-value');
myLink.setAttribute('data-test_attr1', 'another-test-value');
console.log(myLink.dataset) // Выдаст следующее: DOMStringMap {test_attr: 'test-value', test_attr1: 'another-test-value'} test_attr: "test-value"test_attr1: "another-test-value"[[Prototype]]: DOMStringMap
